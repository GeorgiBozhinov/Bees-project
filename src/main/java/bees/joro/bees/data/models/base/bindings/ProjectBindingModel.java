package bees.joro.bees.data.models.base.bindings;

public class ProjectBindingModel {

    private String first_name;
    private String middle_name;
    private String last_name;
    private String bee_farm_name;
    private String bees_number;
    private String country;
    private String address;
    private String proj_type;
    private String add_inf;


    public ProjectBindingModel() {
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getMiddle_name() {
        return middle_name;
    }

    public void setMiddle_name(String middle_name) {
        this.middle_name = middle_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getBee_farm_name() {
        return bee_farm_name;
    }

    public void setBee_farm_name(String bee_farm_name) {
        this.bee_farm_name = bee_farm_name;
    }

    public String getBees_number() {
        return bees_number;
    }

    public void setBees_number(String bees_number) {
        this.bees_number = bees_number;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getProj_type() {
        return proj_type;
    }

    public void setProj_type(String proj_type) {
        this.proj_type = proj_type;
    }

    public String getAdd_inf() {
        return add_inf;
    }

    public void setAdd_inf(String add_inf) {
        this.add_inf = add_inf;
    }
}
